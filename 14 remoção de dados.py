# Lista de animais
animais = ["Cachorro", "Gato", "Peixe", "Papagaio"]

# Remover pelo valor
animais.remove("Peixe")

# Remover pelo índice
animais.pop(0)  # Remove o primeiro

# Deletar elemento específico
del animais[1]  # Remove o segundo (atual)

# Limpar a lista
animais.clear()

print(animais)  # Lista agora está vazia
